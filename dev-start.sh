#!/bin/bash

# =============================================================================
# FullStackFastAPI 快速启动脚本（项目根目录）
# =============================================================================

# 检查是否在项目根目录
if [ ! -f "docker-compose.yml" ]; then
    echo "错误：请在项目根目录执行此脚本"
    exit 1
fi

# 执行完整的开发环境启动脚本
exec ./scripts/local-dev.sh "$@"